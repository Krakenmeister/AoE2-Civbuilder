function addSpecialNodes(specialNodes, nodeList, prerequisite, beforeNode = "") {
  for (let specialNode of specialNodes) {
    let node = JSON.parse(JSON.stringify(specialNode));
    if (prerequisite == 1) {
      node["Node Status"] = "ResearchedCompleted";
    } else {
      node["Node Status"] = "NotAvailable";
    }
    if (beforeNode == "") {
      nodeList.push(node);
    } else {
      for (let i = 0; i < nodeList.length; i++) {
        if (nodeList[i]["Name"] == beforeNode) {
          nodeList.splice(i, 0, node);
          i = nodeList.length;
        }
      }
    }
  }
}

function removeNode(nodeName, nodeList) {
  for (let i = 0; i < nodeList.length; i++) {
    if (nodeList[i]["Name"] == nodeName) {
      nodeList.splice(i, 1);
      i--;
    }
  }
}

